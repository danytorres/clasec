#include <stdio.h>

int main(void){
printf( "10 + 3 = %hd\n", 10 + 3 );    // Operador de adición
  printf( "10 - 3 = %hd\n", 10 - 3 );    // Operador de subtracción
  printf( "10 / 3 = %hd\n", 10 / 3 );    // Operador de división
  printf( "10 / 3 = %f\n", 10 / 3 );     // Operador de división
  printf( "10 / 3 = %f\n", 10 / 3.0 );   // Operador de división
  printf( "10 * 3.5 = %hd\n", 10 * 3.5 );// Operador de multiplicación
  printf( "10 * 3.5 = %f\n", 10 * 3.5 ); // Operador de multiplicación
  printf( "10 %% 3 = %hd\n", 10 % 3 );   // Operador de modulo
  printf( "Listo.\n" );
 
  return 0;
}